#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	seter;

	i = 0;
	n = 1;
	seter = 0;
	while (str[i] != '\0' && (str[i] == 32 || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		seter *= 10;
		seter += str[i] - 48;
		i++;
	}
	return (seter * n);
}
