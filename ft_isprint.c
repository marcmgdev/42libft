#include "libft.h"

int	ft_isprint(int c)
{
	if (c >= 040 && c <= 047 || c >= 050 && c <= 57
		|| c >= 060 && c <= 067 || c >= 070 && c <= 077
		|| c >= 100 && c <= 107 || c >= 110 && c <=117
		|| c >= 120 && c <= 127 || c >= 130 && c <= 137
		|| c >= 140 && c <= 147 || c >= 150 && c <= 157
		|| c >= 160 && c <+ 167 || c >= 170 && c <= 176)
		return (1);
	else
		return (0);
}
